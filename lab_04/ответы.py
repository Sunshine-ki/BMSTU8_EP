# Что является ядром ОЦКП?  Либо ПФЭ, либо некоторые ДФЭ.
# - Какие ДФЭ могут быть ядром ОЦКП?
# Такие ДФЭ, у которых парные взаимодействия не смешиваются друг с другом
# и с линейными взаимодействиями
# Какие дробные реплики могут быть ядром ОЦКП?
# 
# Почему парные взаимодействия не должны смешиваться?
# Потому что мы строим полином второй степени. При смешении парных
# коэффициентов сильно падает точность.
# 
# Сколько парных взаимодействий будет при n факторах?
# n-1 + n-2 + n-3 + … + 1 = n*(n-1)/2
# Это арифметическая прогрессия, в которой n слагаемых. Первый фактор может
# взаимодействовать со всеми остальными (их будет n-1), второй с n-2 (тоже со
# всеми остальными кроме первого, потому что это взаимодействие учитывается
# в первом слагаемом) и т.д.

# Как расшифровывается ОЦКП?
# ОЦКП - это ортогональное центральное композиционное планирование.
# Ортогональное - скалярное произведение любых 2 столбцов плана дает 0

# Центральное - все точки симметричны относительно центра плана.
# Композиционное - состоит из нескольких частей, а именно: ядро плана,
# "звездные" точки и центр плана.

# Можно ли для получения нелинейной модели использовать ПФЭ?
# Можно, если используются 3 и более уровней варьирования факторов (для
# построения нелинейной модели нужно 3 уровня)